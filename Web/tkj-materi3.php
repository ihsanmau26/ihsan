    <h1 class="judul">Peralatan Vital Membangun Suatu Jaringan dan Fungsinya</h1> <br><br>
    <p>
      Membangun suatu jaringan komputer, tentunya memiliki peralatan-peralatan vital atau syarat utama dalam membangun jaringan tersebut. dan yang harus dimiliki untuk membangun sebuah jaringan beserta fungsinya masing-masing dalam jaringan tersebut adalah:
    </p>
    <p>
      1. Komputer Server: Fungsi komputer Server adalah sebagai pusat data sebagai pintu masuk ke dalam sistem jaringan dan berisikan daftar user yang diperbolehkan masuk ke server atau kedalam sistem jaringan tersebut.
    </p>
    <p>
      2. Komputer workstation (client): Komputer client berfungsi  memanfaatkan jaringan untuk menghubungkan komputer tersebut dengan komputer lain atau komputer tersebut dengan server. Pemanfaatan jaringan tersebut dapat berupa sharing data, sharing printer dan sebagainya.
    </p>
    <p>
      3. HUB/switch: Hub/switch berfungsi sebagai terminal atau pembagi sinyal data bagi kartu jaringan (Network Card).
    </p>
    <p>
      4. Kartu jaringan NIC: Sebuah kartu jaringan (LAN Card) yang terpasang pada sebuah komputer server maupun client berfungsi sebagai media untuk penghubung sehingga komputer dapat dihubungkan kedalam sistem jaringan.
    </p>
    <p>
      5. Kabel dan Konektor: Kabel dan konektor berfungsi sebagai media penghubung antara komputer client dengan komputer client yang lain atau dengan peralatan lain yang digunakan untuk membentuk jaringan.
    </p>
