    <h1 class="judul">FTTH (Fiber To The Home)</h1> <br><br>
    <p>
      Fiber To The Home atau yang disebut dengan FTTH, adalah suatu jaringan akses atau jaringan yang menghubungkan antara pusat layanan dengan peralatan pelanggan atau Customer Premises Equipment (CPE) dengan menggunakan Fiber Optik.
    </p>
    <p>
      Alasan mengapa harus menggunakan Fiber Optik:<br>
      a. Dapat menyalurkan informasi dengan kecepatan yang sangat tinggi, atau berpita lebar High Speed Data ( > 100 Mbps)<br>
      b. Dalam satu Fiber Optik dapat menyalurkan informasi dua arah (transmit dan receive), hal ini berbeda dengan saluran tembaga yang memerlukan dua saluran yang berbeda untuk arah kirim dan arah terima.<br>
      c. Karena dapat menyalurkan High Speed Information, maka dalam satu fiber dapat melayan Voice, Video dan Data atau yang disebut dengan layanan Triple Play.<br>
      d. Mempunyai sifat redaman/attenuation yang relatif kecil, sehingga jarak yang direkomendasi dari Pusat Layanan sampai dengan Pelanggan adalah 20 Kilometer, disamping itu noise maupun distorsi relatif kecil sekali.<br>
      e. Secara teknologi, sistem menggunakan spliter atau satu fiber dapat dicabang ke beberapa pelanggan, sehingga sangat effesien dalam pembangunan jaringan.<br>
      f. Sangat memungkinkan untuk pengembangan teknologi jaringan yang memerlukan kecepatan tinggi, misal untuk Jaringan ke Base Station.
    </p>
