    <h1 class="judul">Saluran Transmisi TELKOM</h1> <br><br>
    <p>
      Saluran transmisi adalah penghantar, baik berupa konduktor ataupun isolator (dielektrika), yang digunakan untuk menghubungkan suatu pembangkit sinyal, disebut juga sumber, dengan sebuah penerima/pemakai atau disebut juga beban. Karena sinyal elektrik merambat „hanya‟ dengan kecepatan cahaya, maka sinyal elektrik juga memerlukan suatu waktu tempuh tertentu untuk merambat dari suatu tempat. Saluran transmisi adalah jalur fisik antara pemancar dan penerima. Baik sinyal analog maupun digital dapat dipancarkan melalui saluran transmisi yang sesuai. Pilihan media transmisi (pengirim) untuk keperluan komunikasi data tergantung pada beberapa faktor, seperti harga, performance jaringan yang dikehendaki, ada atau tidaknya medium tersebut.
    </p>
    <p>
      Dalam bidang telekomunikasi, media penghubung atau saluran transmisi, dapat dikategorikan dalam dua kelompok, yaitu: Pertama, SALURAN FISIK, yaitu saluran yang mempunyai bentuk serta ukuran fisik (solid). Kedua, SALURAN NON FISIK, yaitu saluran yang tidak berbentuk, dan hanya ada satu di alam, yaitu udara. Sinyal akan merambat melalui udara dengan frekuensi yang berbeda. Rambatan sinyal juga ditentukan dengan panjang gelombang.Kecepatan rambat sinyal pada media udara sama dengan kecepatan cahaya, yaitu, c= 2,99792458 x 108 m/detik.
