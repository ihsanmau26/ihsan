    <h1 class="judul">Media Saluran Transmisi</h1> <br><br>
    <p>
      1. Guided Transmission Media: Guided transmission media merupakan sistem transmisi jaringan yang menggunakan sistem kabel. Media Guided sangat dipengaruhi oleh karakteristik media. Kapasitas transmisi, baik dalam hal data rate maupun bandwidth sangat tergantung pada jarak dan sistem transmisi medianya (point to point atau multipoint; LAN).
    <P>
      Media guided yang umum digunakan:<br>
      a. TWISTED PAIR Media yang paling banyak digunakan dalam jaringan telepon. Banyak digunakan untuk komunikasi didalam suatu bangunan gedung. Digunakan pada sistem Private Branch Exchange (PBX). Pensinyalan Analog (misal: telepon rumah). Pensinyalan Digital (misal: LAN).<br>
      b. COAXIAL CABLE Lebih tahan terhadap Interferensi dan Crosstalk dibandingkan Twisted Pair. Medium paling banyak digunakan. Distribusi TV, Cable TV, Transmisi telepon jarak jauh. Dapat memuat 10,000 percakapan secara bersamaan. Sambungan sistem komputer jarak dekat, Local area networks.
      Karakteristik Transmisi kabel coaxial: Analog, Dikuatkan setiap beberapa km, Jarak lebih dekat bila frequency lebih tinggi, Sampai 500MHz, Digital, Repeater setiap 1 km, Lebih dekat untuk data rate lebih tinggic. Fiber Optic<br>
      c. FIBER OPTIC Menggunakan cahaya untuk mengirim data. Cahaya memiliki bandwidth lebih lebar daripada gelombang elektromagnetik. Attenuasi rendah, Kecepatan data tinggi, Jarak jangkauan jauh, Keamanan baik, sulit untuk dibajak. Mahal serta instalasi sulit.
    </p>
    <p>
      Keuntungan fiber optic: Kapasitas lebih besar, Data rate mencapai ratusan Gbps, Ukuran lebih kecil dan bobot ringan, Attenuasi lebih rendah, Isolasi Electromagnetic, Tidak dipengaruhi oleh medan elektromagnetik eksternal, sehingga tidak mudah diserang interferensi, derau maupun crosstalk, Spasi repeater lebih besar, Paling sedikit puluhan km
    </p>
    <p>
      2. Guided Transmission Media: Unguided transmission media merupakan transmisi media jaringan gelombang. Media Unguided sangat dipengaruhi oleh Karakteristik Sinyal- Jenis-jenis dari unguided transmission media:<br>
      • Gelombang mikro: gelombang radio yang beroperasi pada frekuensi tinggi(UHF,SHF dan EHF)<br>
      • Satelit: menerima sinyal dari stasiun bumi dan meneruskannya ke stasiun bumi lainnya. Keuntungannya lebih murah daripada menggelar kabel ke seluruh benua.<br>
      • Gelombang radio: gelombang transmisi yang digunakan untuk mengirim data atau suara. Dapat mengirimkan gelombang dengan posisi sembarang dan memungkinkan dalam keadaan bergerak.<br>
      • Infra merah: digunakan untuk komunikasi jarak dekat dengan kecepatan 4Mbps. Contoh penggunaannya bisa berupa remote control televisi. Kebal terhadap interferensi gelombang radio.
    </p>
