    <h1 class="judul">Macam-macam Ganggaun Saluran Transmisi</h1> <br><br>
    <p>
      1. Gangguan Random adalah gangguan yang tidak dapat diramalkan terjadinya, yang termasuk gangguan jenis random yaitu:<br>    
      a. Derau panas yang disebabkan oleh pergerakan acak electron bebas dalam rangkaian. Pada beranda seluruh spekrum yang tersedia disebut juga derau putih atau disebut dengan white noise atau juga derau gausian. Dan kejadiannya tidak dapat dihindarkan dan biasanya tidak terlalu mengganggu transmisi data kecuali jika lebih besar dari sinyal yang ditransmisi.<br>  
      b. Gema yaitu sinyal yang dipantulkan kembali. Karena hal ini disebabkan oleh adanya perubahan impedasi pada rangkaian listrik contoh: sambungan andara dua potong kawat yang dia meternya beda.<br>
      c. Derau impulse atau spikes yaitu tegangan yang tingginya lebih besar disbanding tengangan atau rataâ€ dan sumbernya antara lain yaitu perubahan tegangan pada saluran listrik, pendekatan dengan saluran komunikasi data, perubahan tegangan pada motor, switch untuk penerangan dan sebagainya.<br> 
      d. Perubahan sudut yaitu kadang sinyal dapat berubah karena impulse noise, sudut yang dapat berubah dan kemudian kembali menjadi normal lagi.<br> 
      e. Bicara silang atau cross talk yaitu disebabkan oleh masuknya sinyal dari kanal lain yang letaknya berdekatan. Dan biasanya terjadi pada salurantelepon yang berdekatan atau dimultiplek. Bicara silang akan semakin jelas atau bertambah apabila jarak yang ditempuh semakin jauh. Sinyal yang ditransmisikan semakin kuat atau semakin besar frekuensinya.<br> 
      f. Fading terutama yang terjadi pada system gelombang mikro antara lain selective fading yaitu yang disebabkan pada atmosfer dan sinyal yang disalurkan hingga penerima melalui berbagai jalur. Sinyal ini kalau kemudian bergabung maka hasilnya akan terganggu.<br> 
      g. Derau intermodulasi yaitu dua sinyal dari saluran yang berbeda akan membentuk sinyal baru yang menduduki frekuensi sinyal lain, intermodulasi ini dapat terjadi pada transmisi data apabila modem menggunakan satu frekuensi untuk menjaga agar saluran sinkron selama data tidak dikirim. Frekuwensi ini dapat memodulasi sinyal yang ada pada saluran lain.<br> 
      h. Jitter timbul oleh system pembawa yang di multiple yang dihasilkan perubahan frekwensi dan sudut sinyal berubah sehingga menyebabkan kesukaran dalam mendeteksi bentuk sinyal tersebut.
    </p>
    <p>
      2. Gangguang Tak Random adalah gangguan yang terjadinya dapat diramalkan dan diperhitungkan. Yang termasuk gangguan ini adalah:<br>
      a. Redaman yaitu tegangan dalam suatu sinyal berkurang ketika melalui saluran transmisi. Dan hal ini disebabkan oleh karena daya diserap saluran transmisi dan tergantung pada frekwensi sinyal jenis media transmisi dan panjang jarak saluran transmisi. Redaman tidak sama besarnya untuk semua frekwensi.<br> 
      b. Tundaan pada sinyal umumnya terdiri atas banyak frekwensi. Dan masing frekwensi tidak berjalan dengan cepat yang sama sehingga tiba dipesawat penerima pada waktu yang berbeda, tundaan yang terlalu besar akan menimbulkan kesalahan pada trnsmisi data jadi tundaan ini bukan merupakan gangguan yang serius terhadap transmisi suara tetap pada transmisi data akan menyebabkan terjadinya kesalahan
    </p>
